let currentLang: Lang = (typeof localStorage !== 'undefined' && (localStorage.getItem('fl_lang') as Lang)) || 'zh';

export function setLang(l: Lang) {
  currentLang = l;
  if (typeof localStorage !== 'undefined') localStorage.setItem('fl_lang', l);
}

export function getLang(): Lang {
  return currentLang;
}

export function t(key: string, params?: Record<string, string | number>): string {
  const raw = dict[currentLang]?.[key] || dict['zh'][key] || key;
  if (!params) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? ''));
}

// React hook: 订阅语言变化,自动触发重渲染
export function useT() {
  const [, force] = useState(0);
  useEffect(() => {
    const h = () => force(x => x + 1);
    window.addEventListener('fl-lang-change', h);
    return () => window.removeEventListener('fl-lang-change', h);
  }, []);
  return t;
}
